int	ft_str_is_printable(char *str)
{
	int	i;

	i = -1;
	if (str[0] == 0)
		return (1);
	while (str[++i])
	{
		if (str[i] < ' ' || str[i] == 127)
			return (0);
	}
	return (1);
}
